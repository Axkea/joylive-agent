package com.jd.live.agent.plugin.router.dubbo.v2_6.definition;

import com.jd.live.agent.bootstrap.classloader.ResourcerType;
import com.jd.live.agent.core.bytekit.matcher.MatcherBuilder;
import com.jd.live.agent.core.extension.annotation.ConditionalOnClass;
import com.jd.live.agent.core.extension.annotation.ConditionalOnProperty;
import com.jd.live.agent.core.extension.annotation.Extension;
import com.jd.live.agent.core.inject.annotation.Inject;
import com.jd.live.agent.core.inject.annotation.InjectLoader;
import com.jd.live.agent.core.inject.annotation.Injectable;
import com.jd.live.agent.core.plugin.definition.InterceptorDefinition;
import com.jd.live.agent.core.plugin.definition.InterceptorDefinitionAdapter;
import com.jd.live.agent.core.plugin.definition.PluginDefinitionAdapter;
import com.jd.live.agent.governance.config.GovernanceConfig;
import com.jd.live.agent.governance.invoke.InvocationContext;
import com.jd.live.agent.governance.invoke.filter.OutboundFilter;
import com.jd.live.agent.plugin.router.dubbo.v2_6.interceptor.MonitorFilterInterceptor;

import java.util.List;

@Injectable
@Extension(value = "MonitorFilterDefinition_v2.6")
@ConditionalOnProperty(value = GovernanceConfig.CONFIG_LIVE_ENABLED, matchIfMissing = true)
@ConditionalOnProperty(value = GovernanceConfig.CONFIG_LIVE_DUBBO_ENABLED, matchIfMissing = true)
@ConditionalOnProperty(value = GovernanceConfig.CONFIG_REGISTRY_ENABLED, matchIfMissing = true)
@ConditionalOnProperty(value = GovernanceConfig.CONFIG_TRANSMISSION_ENABLED, matchIfMissing = true)
@ConditionalOnClass(MonitorFilterDefinition.TYPE_MONITOR_FILTER)
public class MonitorFilterDefinition extends PluginDefinitionAdapter {

    protected static final String TYPE_MONITOR_FILTER = "com.alibaba.dubbo.monitor.support.MonitorFilter";

    private static final String METHOD_INVOKE = "invoke";

    private static final String[] ARGUMENT_INVOKE = new String[]{
            "com.alibaba.dubbo.rpc.Invoker",
            "com.alibaba.dubbo.rpc.Invocation"
    };

    @Inject(InvocationContext.COMPONENT_INVOCATION_CONTEXT)
    private InvocationContext context;

    @Inject
    @InjectLoader(ResourcerType.PLUGIN)
    private List<OutboundFilter> filters;

    public MonitorFilterDefinition() {
        this.matcher = () -> MatcherBuilder.named(TYPE_MONITOR_FILTER);
        this.interceptors = new InterceptorDefinition[]{
                new InterceptorDefinitionAdapter(
                        MatcherBuilder.named(METHOD_INVOKE).
                                and(MatcherBuilder.arguments(ARGUMENT_INVOKE)),
                        () -> new MonitorFilterInterceptor(context, filters)
                )
        };
    }
}
